export function getEventsByProgram(programSlug: string): Event[] {
  return events.filter((e) => e.programSlug === programSlug);
}

export function getUpcomingEvents(): Event[] {
  return events
    .filter((e) => e.status === "Upcoming" || e.status === "Active")
    .sort((a, b) => b.year - a.year);
}
